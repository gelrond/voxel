// ********************************************************************************************************************
import { List } from './list';
// ********************************************************************************************************************
import { Vector3 } from './vector3';
// ********************************************************************************************************************
export class Vector3List extends List<Vector3> {

    // ****************************************************************************************************************
    // constructor
    // ****************************************************************************************************************
    constructor(vectors: Vector3[] = []) { super(vectors); }

    // ****************************************************************************************************************
    // function:    sortByDistance
    // ****************************************************************************************************************
    // parameters:  origin - the origin
    // ****************************************************************************************************************
    // returns:     n/a
    // ****************************************************************************************************************
    public sortByDistance(origin: Vector3): void {

        this.array.sort((vector1, vector2) => {

            const distance1 = vector1.distanceTo(origin);

            const distance2 = vector2.distanceTo(origin);

            return distance1 - distance2;
        })
    }
}
