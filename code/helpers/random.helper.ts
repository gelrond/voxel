// ********************************************************************************************************************
import * as THREE from 'three';
// ********************************************************************************************************************

// ********************************************************************************************************************
// function:    random
// ********************************************************************************************************************
// parameters:  min - the min
// ********************************************************************************************************************
//              max - the max
// ********************************************************************************************************************
// returns:     the result
// ********************************************************************************************************************
export function random(min: number, max: number): number {

    const result = THREE.MathUtils.randFloat(min, max);

    return result;
}

// ********************************************************************************************************************
// function:    randomChance
// ********************************************************************************************************************
// parameters:  chance - the chance
// ********************************************************************************************************************
// returns:     the result
// ********************************************************************************************************************
export function randomChance(chance: number): boolean {

    const result = randomZeroOne() > chance;

    return result;
}

// ********************************************************************************************************************
// function:    randomD6
// ********************************************************************************************************************
// parameters:  n/a
// ********************************************************************************************************************
// returns:     the result
// ********************************************************************************************************************
export function randomD6(): number {

    const result = randomInteger(1, 6);

    return result;
}

// ********************************************************************************************************************
// function:    randomD20
// ********************************************************************************************************************
// parameters:  n/a
// ********************************************************************************************************************
// returns:     the result
// ********************************************************************************************************************
export function randomD20(): number {

    const result = randomInteger(1, 20);

    return result;
}

// ********************************************************************************************************************
// function:    randomInteger
// ********************************************************************************************************************
// parameters:  min - the min
// ********************************************************************************************************************
//              max - the max
// ********************************************************************************************************************
// returns:     the result
// ********************************************************************************************************************
export function randomInteger(min: number, max: number): number {

    const result = THREE.MathUtils.randInt(min, max);

    return result;
}

// ********************************************************************************************************************
// function:    randomZeroOne
// ********************************************************************************************************************
// parameters:  n/a
// ********************************************************************************************************************
// returns:     the result
// ********************************************************************************************************************
export function randomZeroOne(): number {

    const result = random(0, 1);

    return result;
}
